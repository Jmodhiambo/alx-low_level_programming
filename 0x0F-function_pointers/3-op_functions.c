#include "3-calc.h"

/**
* op_add - add two numbers
* @a: the first number
* @b: the second number
*
* Return: returns the addiotion of the two numbers
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - subtracts two numbers
* @a: the first number
* @b: the second number
*
* Return: returns the difference of the two numbers
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - multiplies two numbers
* @a: the first number
* @b: the second number
*
* Return: returns the product of the two numbers
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - divides two numbers
* @a: the first number
* @b: the second number
*
* Return: returns the division of the two numbers
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - returns modulus of a number
* @a: the first number
* @b: the second number
*
* Return: returns the remainder of the division the two numbers
*/

int op_mod(int a, int b)
{
	return (a % b);
}
